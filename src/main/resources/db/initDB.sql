DROP TABLE CUSTOMER;
DROP TABLE PHONE;

CREATE TABLE CUSTOMER
(
  id         INTEGER GENERATED BY DEFAULT AS IDENTITY,
  name       VARCHAR(255),
  patronymic VARCHAR(255),
  surname    VARCHAR(255),
  phone_id   INTEGER,
  PRIMARY KEY (id)
);

CREATE TABLE PHONE
(
  id          INTEGER GENERATED BY DEFAULT AS IDENTITY,
  description VARCHAR(255),
  number      VARCHAR(255) NOT NULL,
  phone_type  VARCHAR(255),
  PRIMARY KEY (id)
);
